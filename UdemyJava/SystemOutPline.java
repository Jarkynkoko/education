package UdemyJava;

public class SystemOutPline {
    public static void main(String[] args) {

        // \n --> new line
        // \t --> tab
        // \back --> backspace removes the character befor it
        System.out.print("Cat");
        System.out.print("Dog");
        System.out.print("Rabbit\n");

        System.out.println("Cat");
        System.out.println("Dog");
        System.out.println("Camel");

        System.out.println("Rat\n");
        System.out.println("Dog\n");
        System.out.println("Pets");

        System.out.println("Rat\bDog\bRats");
        System.out.println("Ra\n\n\tbbit");

        System.out.println("The cat said \"Meow\", and run away");
        System.out.println("C:\\User\\bob");

    }
}
