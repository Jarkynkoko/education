package Operators;

public class incrementPreicrement {
    public static void main(String[] args) {

        int book = 10;
        System.out.println(book ++);//10 we have then incremented 11
        System.out.println(++book);// 11 increment 12 then it prints
        System.out.println(book);//12
        System.out.println(book--);;// 12 then decremented 11
        System.out.println(--book);;// 11 ---10
        System.out.println(book);// 10
    }
}
